//add function
export let addfunction = (num1, num2) => {
  return num1 + num2;
};

//sub function
export let subfunction = (num1, num2) => {
  return num1 - num2;
};

//multiply function
export let mulfunction = (num1, num2) => {
  return num1 * num2;
};

//div function
export let divfunction = (num1, num2) => {
  return num1 / num2;
};
